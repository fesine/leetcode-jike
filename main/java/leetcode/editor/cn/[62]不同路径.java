    public int uniquePaths(int m, int n) {
        int[][] dp = new int[m][n];
        //æœ€åä¸€è¡Œ
        for (int i = 0; i < m; i++) {
            dp[i][n-1] = 1;
        }
        //æœ€åä¸€åˆ—
        for (int i = 0; i < n; i++) {
            dp[m-1][i] = 1;
        }
        for (int i = m-2; i >= 0; i--) {
            for (int j = n-2; j >= 0; j--) {
                dp[i][j] = dp[i][j+1]+dp[i+1][j];
            }
        }
        return dp[0][0];

    }

    public static void main(String[] args) {
        System.out.println(new UniquePaths().uniquePaths(7,3));
    }
